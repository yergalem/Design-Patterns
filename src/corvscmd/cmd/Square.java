package corvscmd.cmd;

public class Square {
  
	int side;
	Coordinate pos;
	
	public Square() {
	}
	public Square( int side ) {
		this.side = side;
	}
	public int getSide() {
		return side;
	}
	public void setSide(int side) {
		this.side = side;
	}
	public Coordinate getPos() {
		return pos;
	}
	public void setPos(Coordinate pos) {
		this.pos = pos;
	}
	
	
	
	
}
